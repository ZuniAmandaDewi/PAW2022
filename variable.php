<?php
	$angka1 = 6;
	$angka2 = 2;

	//pejumlahan
	$penjumlahan = $angka1 + $angka2;
	echo "penjumlahan ".$angka1. " + ". $angka2. " adalah = ". $penjumlahan;

	//pengurangan
	$pengurangan = $angka1 - $angka2;
	echo "penjumlahan ".$angka1. " - ". $angka2. " adalah = ". $pengurangan;

	//perkalian
	$perkalian = $angka1 * $angka2;
	echo "penjumlahan ".$angka1. " * ". $angka2. " adalah = ". $perkalian;
?>